function sortbyprice(){
    //var select = document.querySelector("#price").value;

    var selection = document.querySelector("#price").value;

    console.log(selection)

    if(selection=="select"){
        Seller(seller);
    }

    if(selection == "LTH"){
        seller.sort(function(a,b){
            return a.print - b.print;
        })
        Seller(seller);
    }

    if(selection == "HTL"){
        seller.sort(function(a,b){
            return b.print - a.print;
        })
        Seller(seller);
    }
}


function Seller(seller){
    document.querySelector("#utkk").innerText = "";







seller.map(function(ele){

    var box = document.createElement("div");

    var img = document.createElement("img");
    img.src = ele.imgPro;
    img.setAttribute("class","image");

    var name = document.createElement("p");
    name.innerText = ele.name;
    name.style.textAlign="center";

    var print = document.createElement("h3");
    print.innerText = ele.print;
    print.style.textAlign= "center";

    var btn = document.createElement("button");
    btn.innerText = "Add to Cart";
    btn.setAttribute("class","btn-btn")

    box.append(img,name,print,btn)
    document.querySelector("#utkk").append(box)

    btn.addEventListener("click",function(){
        product(ele);
    })
    img.addEventListener("click",function(){
        productd(ele)
    })
});
}


